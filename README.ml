Schenectady Voter Registration Map

This project is a browser-based interactive map that displays voter registration statistics by district in the City of Schenectady. It is designed to be simple to use, free to host, and accessible to users with no technical background.

The map allows users to click on a district to view voter totals, demographic breakdowns, and comparisons to citywide data.

---

## How to Use (No Setup Required)

1. Open the website link in any modern web browser.
2. Zoom and pan the map as needed.
3. Click on a district to view voter statistics and charts.

No software installation, accounts, or configuration is required for viewers.

